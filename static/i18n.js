const sharedLangStorageKey = "miauzap_lang";
const tempLangStorageKey = "miauzap_lang_temp";

function normalizeLanguage(lang) {
    return translations[lang] ? lang : "pt";
}

function getLegacyLangStorageKey() {
    const adminToken = localStorage.getItem('adminToken') || localStorage.getItem('admintoken');
    const userToken = localStorage.getItem('token');
    if (adminToken) return 'miauzap_lang_admin';
    if (userToken) return 'miauzap_lang_' + userToken;
    return null;
}

function getSavedLanguage() {
    const sharedLang = localStorage.getItem(sharedLangStorageKey);
    if (sharedLang) return normalizeLanguage(sharedLang);

    const tempLang = sessionStorage.getItem(tempLangStorageKey);
    if (tempLang) return normalizeLanguage(tempLang);

    const legacyKey = getLegacyLangStorageKey();
    if (legacyKey) {
        const legacyLang = localStorage.getItem(legacyKey);
        if (legacyLang) return normalizeLanguage(legacyLang);
    }

    return "pt";
}

function saveLanguage(lang) {
    const normalizedLang = normalizeLanguage(lang);
    localStorage.setItem(sharedLangStorageKey, normalizedLang);
    sessionStorage.setItem(tempLangStorageKey, normalizedLang);

    const legacyKey = getLegacyLangStorageKey();
    if (legacyKey) {
        localStorage.setItem(legacyKey, normalizedLang);
    }

    return normalizedLang;
}

function changeLanguage(lang) {
    lang = saveLanguage(lang);

    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
    placeholders.forEach((el) => {
        const key = el.getAttribute("data-i18n-placeholder");
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update select box if exists
    const langSelect = document.getElementById("langSelect");
    if (langSelect) {
        langSelect.value = lang;
    }

    const instanceLanguageSelect = document.getElementById("instanceLanguageSelect");
    if (instanceLanguageSelect) {
        instanceLanguageSelect.value = lang;
    }

    // Update Semantic UI dropdown if exists and jQuery is loaded
    if (window.jQuery) {
        const $dashLangSelector = jQuery('#dashLangSelector');
        if ($dashLangSelector.length) {
            $dashLangSelector.dropdown('set selected', lang);
        }
    }

    // Update API Payload Models if available (dashboard)
    if (window.updateDashboardLanguage) {
        window.updateDashboardLanguage(lang);
    }
}

window.i18n = {
    changeLanguage,
    getSavedLanguage,
    applyTranslations: function () {
        changeLanguage(getSavedLanguage());
    }
};

// Initialization on load
document.addEventListener("DOMContentLoaded", () => {
    changeLanguage(getSavedLanguage());
});
